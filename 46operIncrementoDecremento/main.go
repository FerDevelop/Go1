package main

import "fmt"

func main() {
	//valor inicial de la variable

	a := 0

	//operador de incremento
	a++ // valdra 1
	a++ // valdra 2
	a++ // valdra 3
	a++ // valdra 4
	a++ // valdra 5
	fmt.Println(a)

	//operador de decremento
	a-- // valdra 4
	a-- // valdra 3
	a-- // valdra 2
	a-- // valdra 1
	a-- // valdra 0
	fmt.Println(a)
}
